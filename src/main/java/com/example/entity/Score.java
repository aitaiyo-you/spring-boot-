package com.example.entity;

public class Score {
    private Integer id;
    private Integer sid;
    private Integer cid;
    private Float usual;
    private Float finall;
    private Float comp;
    private String tname;
    private String cname;
    private String sname;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSid() {
        return sid;
    }

    public void setSid(Integer sid) {
        this.sid = sid;
    }

    public Integer getCid() {
        return cid;
    }

    public void setCid(Integer cid) {
        this.cid = cid;
    }

    public Float getUsual() {
        return usual;
    }

    public void setUsual(Float usual) {
        this.usual = usual;
    }

    public Float getFinall() {
        return finall;
    }

    public void setFinall(Float finall) {
        this.finall = finall;
    }

    public Float getComp() {
        return comp;
    }

    public void setComp(Float comp) {
        this.comp = comp;
    }

    public String getTname() {
        return tname;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public String getSname() {
        return sname;
    }

    public void setSname(String sname) {
        this.sname = sname;
    }
}
